// виведення fizz замість чисел, кратних 3;
// виведення buzz замість чисел, кратних 5;
// виведення fizzbuzz замість чисел, кратних як 3, і 5.
// приклад
// Fizzbuzz(5) => Результат
// // 1
// // 2
// // fizz
// // 4
